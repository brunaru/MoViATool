package br.usp.icmc.movia.annotation.ink;

import org.simpleframework.xml.Element;
import org.simpleframework.xml.Root;

@Root
public class PathAction {

	public static final int LINE_TO = 1;
	
	public static final int MOVE_TO = 2;
	
	@Element
	private float x;
	
	@Element
	private float y;
	
	@Element
	private int type;
	
	public PathAction() {
	}
	
	public PathAction(float x, float y, int type) {
		this.x = x;
		this.y = y;
		this.type = type;
	}
	
	public float getX() {
		return x;
	}
	
	public void setX(float x) {
		this.x = x;
	}
	
	public float getY() {
		return y;
	}
	
	public void setY(float y) {
		this.y = y;
	}
	
	public int getType() {
		return type;
	}
	
	public void setType(int type) {
		this.type = type;
	}
	
}
